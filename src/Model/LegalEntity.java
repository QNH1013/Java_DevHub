package request.Model;


public class LegalEntity {
    private String name = null;
	private String type = null;
	private String doingBusinessAs = null;
	private String taxID = null;
	private String phone = null;
	private String annualCreditCardSalesVolume = null;
	private String hasAcceptedCreditCards = null;
	private String yearsInBusiness = null;
    

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDoingBusinessAs() {
		return doingBusinessAs;
	}
	public void setDoingBusinessAs(String doingBusinessAs) {
		this.doingBusinessAs = doingBusinessAs;
	}
	public String getTaxID() {
		return taxID;
	}
	public void setTaxID(String taxID) {
		this.taxID = taxID;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAnnualCreditCardSalesVolume() {
		return annualCreditCardSalesVolume;
	}
	public void setAnnualCreditCardSalesVolume(String annualCreditCardSalesVolume) {
		this.annualCreditCardSalesVolume = annualCreditCardSalesVolume;
	}
	public String getHasAcceptedCreditCards() {
		return hasAcceptedCreditCards;
	}
	public void setHasAcceptedCreditCards(String hasAcceptedCreditCards) {
		this.hasAcceptedCreditCards = hasAcceptedCreditCards;
	}
	public String getYearsInBusiness() {
		return yearsInBusiness;
	}
	public void setYearsInBusiness(String yearsInBusiness) {
		this.yearsInBusiness = yearsInBusiness;
	}
	
	
}