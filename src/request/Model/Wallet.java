package request.Model;


public class Wallet {
    private String walletSourceType = null;
	private String walletSourceTypeID = null;
    

	public String getWalletSourceType() {
		return walletSourceType;
	}
	public void setWalletSourceType(String walletSourceType) {
		this.walletSourceType = walletSourceType;
	}
	public String getWalletSourceTypeID() {
		return walletSourceTypeID;
	}
	public void setWalletSourceTypeID(String walletSourceTypeID) {
		this.walletSourceTypeID = walletSourceTypeID;
	}
	
	
}